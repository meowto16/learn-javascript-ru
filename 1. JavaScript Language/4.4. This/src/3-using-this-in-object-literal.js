/**
 * Использование "this" в литерале объекта (важность: 5)
 *
 * Здесь функция makeUser возвращает объект.
 * Каким будет результат при обращении к свойству объекта ref ? Почему?
 */

function makeUser() {
  return {
    name: "Джон",
    ref: this
  };
}

let user = makeUser();
alert( user.ref.name ); // Каким будет результат?

// Решение:
// Так как важен только момент вызова метода, то метод тут вызывается не в контексте какого-то объекта.
// Соответственно this == undefined
